// GetCharBitmap returns the bitmap data for a character.
// Returns the space bitmap for unknown characters.
func GetCharBitmap(char rune) [CharHeight]uint8 {
	if bitmap, ok := fontData[char]; ok {
		return bitmap
	}
	return fontData[' ']
}

// MeasureText returns the width of a text string in pixels using default spacing.
func MeasureText(text string) int {
	return MeasureTextWithSpacing(text, CharSpacing)
}

// MeasureTextWithSpacing returns the width of a text string with custom spacing.
func MeasureTextWithSpacing(text string, spacing int) int {
	runes := []rune(text)
	if len(runes) == 0 {
		return 0
	}
	// Width = (charWidth * numChars) + (spacing * (numChars - 1))
	return len(runes)*CharWidth + (len(runes)-1)*spacing
}

// HasBitSet checks if a specific bit (0-4, left to right) is set in a row.
func HasBitSet(row uint8, bitPos int) bool {
	// Bits are stored left to right: bit 0 is the leftmost (position 4 in byte)
	// bit 4 is the rightmost (position 0 in byte)
	return (row & (1 << (4 - bitPos))) != 0
}
